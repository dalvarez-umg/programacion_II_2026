public class semana_03_ejemplo2 {
    public static void main(String[] args) {
        int nota = 85;

        if (nota >= 90) {
            System.out.println("Excelente");
        }  else if (nota >= 75) {
            System.out.println("Muy bien");
        } else if (nota >= 61) {
            System.out.println("Aprobado");
        } else {
            System.out.println("Reprobado");
        }
    }
}
