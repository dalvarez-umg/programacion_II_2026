public class semana_03_do_while {
    public static void main(String[] args) {
        int opcion;

        do {
            System.out.println("1. Calcular compra");
            System.out.println("2. Ver tabla");
            System.out.println("3. Salir");

            opcion = 3;

        } while (opcion != 3);
    }
}
