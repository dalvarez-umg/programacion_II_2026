public class Semana02_Operadores {
    public static void main(String[] args) {
        double nota1 = 80;
        double nota2 = 75;
        double nota3 = 90;

        double suma = nota1 + nota2 + nota3;
        double promedio = suma / 3;

        System.out.println("Suma: " + suma);
        System.out.println("Promedio: " + promedio);
    }
}
